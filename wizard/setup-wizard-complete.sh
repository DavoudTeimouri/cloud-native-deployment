generate_scripts() {
    echo "Generating customized scripts..."
    
    # OS preparation script
    local osoutput="$GENERATED_DIR/scripts/os-prep/linux-hardening.sh"
    mkdir -p "$(dirname "$osoutput")"
    
    ssh ${OPS_LINUX_IP} "cat > ${osoutput} << 'OSSESSION'
#!/bin/bash
# OS Preparation Script - Generated by Wizard for ${CLUSTER_NAME}
# Customized IPs: ${MASTER_IPS} ${WORKER_IPS} ${LB_IPS}
# NTP Servers: ${DNS_SERVER_IPS}
# DNS Servers: ${DNS_SERVER_IPS}

set -euo pipefail

# Configuration from wizard
CLUSTER_NAME="${CLUSTER_NAME}"
DOMAIN_SUFFIX="${DOMAIN_SUFFIX}"
NTP_SERVERS=(${DNS_SERVER_IPS})
DNS_SERVERS=(${DNS_SERVER_IPS})
SEARCH_DOMAINS="${CLUSTER_NAME}.${DOMAIN_SUFFIX} ${DOMAIN_SUFFIX}"

# Master node IPs
MASTER_IPS=(${MASTER_IPS})
WORKER_IPS=(${WORKER_IPS})
LB_IPS=(${LB_IPS})
MON_IPS=(${MON_IPS})
OSD_IPS=(${OSD_IPS})
RGW_IP="${RGW_HOST_IP}"
NEXUS_IP="${NEXUS_HOST_IP}"
HARBOR_IP="${HARBOR_HOST_IP}"

# Function stubs
configure_ntp() {
    echo "Configuring NTP with servers: \${NTP_SERVERS[*]}"
    # Actual configuration would go here
}

configure_dns() {
    echo "Configuring DNS servers: \${DNS_SERVERS[*]}"
    # Actual configuration would go here
}

harden_os() {
    echo "Applying OS hardening for cluster: \${CLUSTER_NAME}"
    # Actual hardening commands would go here
}

# Main
configure_ntp
configure_dns
harden_os

echo "OS preparation complete for cluster: \${CLUSTER_NAME}"
OSSESSION"
    chmod +x "$osoutput" 2>/dev/null || true
    echo "  → $osoutput"
    
    # KubeSpray wrapper
    local ksoutput="$GENERATED_DIR/scripts/kubespray-deploy.sh"
    ssh ${OPS_LINUX_IP} "cat > ${ksoutput} << 'KSSCRIPT'
#!/bin/bash
set -euo pipefail

CLUSTER_NAME="${CLUSTER_NAME}"
INVENTORY_DIR="generated/inventory/\$CLUSTER_NAME"
KUBESPRAY_PATH="../kubespray"

echo "Deploying cluster: \$CLUSTER_NAME"
echo "Using inventory: \$INVENTORY_DIR"

# Pre-flight checks
ansible -i "\$INVENTORY_DIR/hosts.yml" all -m ping

# Run deployment
ansible-playbook -i "\$INVENTORY_DIR/hosts.yml" "\$KUBESPRAY_PATH/cluster.yml"

echo "Deployment complete!"
KSSCRIPT"
    chmod +x "$ksoutput" 2>/dev/null || true
    echo "  → $ksoutput"
    
    # HAProxy deployment script
    local haproxy_output="$GENERATED_DIR/scripts/networking/deploy-haproxy-keepalived.sh"
    mkdir -p "$(dirname "$haproxy_output")"
    
    ssh ${OPS_LINUX_IP} "cat > ${haproxy_output} << 'HAPROXYSCRIPT'
#!/bin/bash
set -euo pipefail

LB_IPS=({{ lb_ips }})
MASTER_IPS=({{ master_ips }})
WORKER_IPS=({{ worker_ips }})
VIP="${HAPROXY_VIP}"

echo "Deploying HAProxy + Keepalived..."
echo "LB IPs: \${LB_IPS[@]}"
echo "VIP: \${VIP}"
HAPROXYSCRIPT"
    chmod +x "$haproxy_output" 2>/dev/null || true
    echo "  → $haproxy_output"
}

