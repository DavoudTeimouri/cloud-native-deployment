generate_network_config() {
    echo "Generating network configuration..."
    
    local template="$TEMPLATE_DIR/networking/network-config.yml.j2"
    local output="$GENERATED_DIR/configs/network-config.yml"
    
    mkdir -p "$(dirname "$output")"
    
    if [[ -f "$template" ]]; then
        envsubst < "$template" > "$output"
        echo "  → $output"
    fi
